import Foundation
import UIKit

// MARK: - Custom Colors
extension UIColor {
    static var primary: UIColor = UIColor(named: "primary") ?? UIColor()
    static var deepBlue: UIColor = UIColor(named: "deepBlue") ?? UIColor()
    static var customGold: UIColor = UIColor(named: "customGold") ?? UIColor()
    static var cleanGreen: UIColor = UIColor(named: "cleanGreen") ?? UIColor()
    static var background: UIColor = UIColor(named: "background") ?? UIColor()
    static var borders: UIColor = UIColor(named: "borders") ?? UIColor()
    static var favoriteRed: UIColor = UIColor(named: "favoriteRed") ?? UIColor()
    static var primerGold: UIColor = UIColor(named: "primerGold") ?? UIColor()
}
